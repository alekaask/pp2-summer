#use type to get the data type of a variable
x = 5
print(type(x))
#output: <class 'int'>

x = "hello world"
print(type(x))
#output: <class 'str'>

x = 20.5
print(type(x))
#output: <class 'float'>

x = True
print(type(x))
#output: <class 'bool'>

x = ["apple", "banana", "cherry"]
print(type(x))
#output: <class 'list'>

x = 1j
print(type(x))
#output: <class 'complex'>

x = {"name" : "John", "age" : 36}
print(type(x))
#output: <class 'dict'>

x = ("apple", "banana", "cherry")
print(type(x))
#output: <class 'tuple'>

x = {"apple", "banana", "cherry"}
print(type(x))
#output: <class 'set'>