print("aleka".capitalize())
#output: Aleka

print("ALEKA".casefold())
#output: aleka

print("aleka".center(20, "x"))
#output: xxxxxxxxxxxalekaxxxxxxxxx

print("aleka".count("a"))
#output: 2

#it will return the encoded version of the string as a bytes object.
print("aleka".encode())
#output: b'aleka'