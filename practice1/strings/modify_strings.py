#upper case 
a = "Hello, World!"
print(a.upper())
#output: HELLO, WORLD!

#lower case
a = "Hello, World!"
print(a.lower())
#output: hello, world!

#remove whitespace
a = " Hello, World! "
print(a.strip())
#output: Hello, World!

#replace string
a = "Hello, World!"
print(a.replace("H", "J"))

#split string
a = "Hello, World!"
print(a.split(","))
#output: ['Hello', ' World!']